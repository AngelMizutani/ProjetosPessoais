    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {
            CuteNotes obj = new CuteNotes();
            obj.setAssunto(txtAssunto.getText());
            obj.setNote(txtNotas.getText());
            
            NotesDao dao = new NotesDao();
            dao.CadastrarNotes(obj);
            JOptionPane.showMessageDialog(null, "Nota salva com sucesso!");
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar: " + erro, "Erro", JOptionPane.WARNING_MESSAGE);
        }
        
        this.Listar();
        this.LimparCampos();
        
        CardLayout c1 = (CardLayout) panRaiz.getLayout();
        c1.show(panRaiz, "panInicio");
    }//GEN-LAST:event_btnSalvarActionPerformed

