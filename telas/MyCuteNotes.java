    public void Listar(){
        try {
            NotesDao dao = new NotesDao();
            List<CuteNotes> listaNotes = dao.listarNotes();
            
            DefaultTableModel modelo = (DefaultTableModel) tblInicio.getModel();
            modelo.setNumRows(0);
            
            for(CuteNotes c: listaNotes){
                modelo.addRow(new Object[]{
                c.getDia(),    
                c.getAssunto(),
                });
            }
        } catch (Exception e) {
        }
    }
    
