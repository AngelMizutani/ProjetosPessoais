    private void tblInicioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblInicioMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 2){
            String assunto = tblInicio.getValueAt(tblInicio.getSelectedRow(),tblInicio.getSelectedColumn()).toString();
            try {
            NotesDao dao = new NotesDao();
            List<CuteNotes> listaNotes = dao.verNotes(assunto);
                for (CuteNotes c : listaNotes){

                    txtTitulo.setText(c.getAssunto());
                    txtVerNota.setText(c.getNote());
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Selecione um Assunto");
            }
            CardLayout c1 = (CardLayout) panRaiz.getLayout();
            c1.show(panRaiz, "panVisualizar");
        }
        
    }//GEN-LAST:event_tblInicioMouseClicked

