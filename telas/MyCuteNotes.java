    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        try {
            CuteNotes obj = new CuteNotes();
            obj.setAssunto(tblInicio.getValueAt(tblInicio.getSelectedRow(),1).toString());
            
            NotesDao dao = new NotesDao();
            dao.excluirNotes(obj);
            
            DefaultTableModel modelo = (DefaultTableModel) tblInicio.getModel();
            modelo.removeRow(tblInicio.getSelectedRow());
            
            JOptionPane.showMessageDialog(null, "Nota excluída com sucesso!");
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir " + e);
        }
        
        
    }//GEN-LAST:event_btnExcluirActionPerformed

